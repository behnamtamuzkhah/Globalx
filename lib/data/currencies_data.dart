/// GlobalX – Comprehensive ISO 4217 Currency Dataset
/// Covers all major active world currencies.
/// Structured for scalability: swap this list with a live API response later.
library;

class CurrencyData {
  final String code;
  final String name;
  final String country;
  final String flag;
  final String symbol;

  const CurrencyData({
    required this.code,
    required this.name,
    required this.country,
    required this.flag,
    required this.symbol,
  });

  Map<String, String> toMap() => {
    'code': code,
    'name': name,
    'country': country,
    'flag': flag,
    'symbol': symbol,
  };

  bool matchesQuery(String query) {
    final q = query.toLowerCase();
    return code.toLowerCase().contains(q) ||
        name.toLowerCase().contains(q) ||
        country.toLowerCase().contains(q);
  }
}

