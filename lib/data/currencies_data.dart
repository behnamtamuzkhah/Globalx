  static CurrencyData? findByCode(String code) {
    try {
      return all.firstWhere((c) => c.code == code);
    } catch (_) {
      return null;
    }
  }

  static List<CurrencyData> search(String query) {
    if (query.isEmpty) return all;
    return all.where((c) => c.matchesQuery(query)).toList();
  }

  static List<CurrencyData> get popular =>
      popularCodes.map((c) => findByCode(c)).whereType<CurrencyData>().toList();
