/// GlobalX – Expanded Provider Network Dataset
/// Covers banks, digital banks, remittance services, FX specialists,
/// crypto exchanges, and mobile wallet / payout providers.
/// Structured for scalability: swap with live API responses later.
library;

enum ProviderCategory {
  traditionalBank,
  digitalBank,
  remittance,
  fxSpecialist,
  cryptoExchange,
  mobileWallet,
}

class ProviderData {
  final String id;
  final String name;
  final ProviderCategory category;
  final String logoUrl;
  final String logoSemanticLabel;
  final List<String> supportedCountries; // ISO 3166-1 alpha-2
  final List<String> supportedCurrencies; // ISO 4217
  final double feeFixed; // flat fee in source currency
  final double feePercent; // percentage of send amount
  final double fxMarginPercent; // spread over mid-market rate
  final String deliveryTimeLabel;
  final int deliveryMinutesMin;
  final int deliveryMinutesMax;
  final List<String> payoutMethods;
  final double trustScore; // 0–5
  final double popularityScore; // 0–100
  final List<String> complianceTags; // e.g. ['FCA', 'FinCEN', 'MAS']
  final bool isAvailable;
  final String tagline;

  const ProviderData({
    required this.id,
    required this.name,
    required this.category,
    required this.logoUrl,
    required this.logoSemanticLabel,
    required this.supportedCountries,
    required this.supportedCurrencies,
    required this.feeFixed,
    required this.feePercent,
    required this.fxMarginPercent,
    required this.deliveryTimeLabel,
    required this.deliveryMinutesMin,
    required this.deliveryMinutesMax,
    required this.payoutMethods,
    required this.trustScore,
    required this.popularityScore,
    required this.complianceTags,
    required this.isAvailable,
    required this.tagline,
  });

  String get categoryLabel {
    switch (category) {
      case ProviderCategory.traditionalBank:
        return 'Traditional Bank';
      case ProviderCategory.digitalBank:
        return 'Digital Bank';
      case ProviderCategory.remittance:
        return 'Remittance';
      case ProviderCategory.fxSpecialist:
        return 'FX Specialist';
      case ProviderCategory.cryptoExchange:
        return 'Crypto Exchange';
      case ProviderCategory.mobileWallet:
        return 'Mobile Wallet';
    }
  }

  double totalFeeForAmount(double amount) =>
      feeFixed + (amount * feePercent / 100);
}

