  static ProviderData? findById(String id) {
    try {
      return all.firstWhere((p) => p.id == id);
    } catch (_) {
      return null;
    }
  }

  static List<ProviderData> byCategory(ProviderCategory category) =>
      all.where((p) => p.category == category).toList();

  static List<ProviderData> supportingCorridor(
    String fromCurrency,
    String toCurrency,
  ) => all
      .where(
        (p) =>
            p.supportedCurrencies.contains(fromCurrency) &&
            p.supportedCurrencies.contains(toCurrency) &&
            p.isAvailable,
      )
      .toList();
